// "You are creating a countdown app. Implement a program that uses a while loop to count down from 10 to 1 and prints each number."

let start = 10; // Initialize the countdown starting value
console.log("Starting Countdown from 10");

while (start >= 0) {
    console.log(start); // Print the current countdown value
    start--; // Decrement the countdown value by 1 in each iteration
}
